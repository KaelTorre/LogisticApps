/// [HttpOverrides] global que agrega [_isrgRootX1Pem] y
/// [_globalSignRootR3Pem] a las raíces por defecto del sistema, para toda
/// la app (no solo el cliente de OSRM) — cubre también los `HttpClient`
/// internos de paquetes de terceros como `flutter_map` (tiles del mapa),
/// que no aceptan inyectar un cliente HTTP propio.
///
/// Se activa una sola vez en `main()` con `HttpOverrides.global = ...`
/// antes de `runApp`.
class TrustedRootsHttpOverrides extends HttpOverrides {
  @override
  HttpClient createHttpClient(SecurityContext? context) {
    final contexto = (context ?? SecurityContext(withTrustedRoots: true))
      ..setTrustedCertificatesBytes(utf8.encode(_isrgRootX1Pem))
      ..setTrustedCertificatesBytes(utf8.encode(_globalSignRootR3Pem));
    return super.createHttpClient(contexto);
  }
}
