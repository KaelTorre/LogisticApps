/// Cliente HTTP por defecto de [OsrmClient]: confía en las raíces del
/// sistema (comportamiento normal) más [_isrgRootX1Pem] explícitamente, para
/// no depender de que el almacén de certificados del equipo la tenga
/// cacheada.
http.Client crearClienteHttpConfiable() {
  final contexto = SecurityContext(withTrustedRoots: true)
    ..setTrustedCertificatesBytes(utf8.encode(_isrgRootX1Pem));
  return io_client.IOClient(HttpClient(context: contexto));
}
